/// Defines enums for task categories, priority levels, and app colors.
///
/// This section defines enums used throughout the app:
///   - `Category`: Represents different task categories
///     (none, personal, shopping, social, focus, work, recreation).
///   - `Priority`: Represents different task priority levels
///     (none, low, medium, high).
///   - `AppColours`: Represents different app theme color options
///     (purple, indigo, blue, green, yellow, orange, red, cyan).
enum Category { none, personal, shopping, social, focus, work, recreation }

enum Priority { none, low, medium, high }

enum AppColours {
  purple,
  indigo,
  blue,
  green,
  yellow,
  orange,
  red,
  cyan,
}
